/// Direction for looping animations
/// </summary>
public enum ImAnimDirection
{
    /// <summary>
    /// play forward
    /// </summary>
    Normal,

    /// <summary>
    /// play backward
    /// </summary>
    Reverse,

    /// <summary>
    /// ping-pong
    /// </summary>
    Alternate,
}
